class Graph0n:
    def __init__(self, sz):
        self.dt = [Node0n() for _ in range(sz)]

    def add(self, vn, val):
        self.dt[vn].add(val)

    def add2(self, vn, val):
        self.dt[vn].add(val)
        self.dt[val].add(vn)

    def get(self, vn, index):
        return self.dt[vn].get(index)

    def get_all(self, vn):
        return self.dt[vn].get_all()

    def size_of(self, vn):
        return self.dt[vn].size()

    def clear(self):
        for node in self.dt:
            node.clear()

class Node0n:
    def __init__(self):
        self.next_vs = []

    def add(self, val):
        self.next_vs.append(val)

    def get(self, ad):
        return self.next_vs[ad]

    def get_all(self):
        return self.next_vs

    def size(self):
        return len(self.next_vs)

    def clear(self):
        self.next_vs.clear()

